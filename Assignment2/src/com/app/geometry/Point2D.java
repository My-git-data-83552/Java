package com.app.geometry;

public class Point2D {
	private int xaxia;
	private int yaxis;

	public Point2D(int xaxia, int yaxis) {
		super();
		this.xaxia = xaxia;
		this.yaxis = yaxis;
	}

	public int getXaxia() {
		return xaxia;
	}

	public void setXaxia(int xaxia) {
		this.xaxia = xaxia;
	}

	public int getYaxis() {
		return yaxis;
	}

	public void setYaxis(int yaxis) {
		this.yaxis = yaxis;
	}

}
